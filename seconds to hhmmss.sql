DECLARE @t INT = -86466

SELECT 
	@t/3600,	--hour
	CAST(@t%3600/60 AS NVARCHAR(2)),	--minute
	@t%3600%60	--second

SELECT 
	CASE WHEN @t <0 THEN  N'-' END + CONCAT(RIGHT(N'00000'+CAST(ABS(@t)/3600 AS NVARCHAR(20)),2), N':',RIGHT(CONCAT(N'0000',CAST(ABS(@t)%3600/60 AS NVARCHAR(20))),2),N':',RIGHT(N'00000'+CAST(ABS(@t)%3600%60 AS NVARCHAR(20)),2))
	
	